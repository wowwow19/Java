package chap04;

public class IterationExam {
	public static void main(String[] args) {
		// 반복문
		
		// for, while, do-while
		
		// for : 반복횟수를 정확히 알고 있을 때 사용, 사용빈도 가장 높음
		
			for (/*초기식*/;/*조건식*/;/*증감식*/) {
				/*문장*/
			}
			// 1. 초기식 : 가장 먼저 수행, 단 한 번만 수행
			// 2. 조건식 : boolean 참, 거짓을 통해 문장 수행 여부를 결정 >> 참일 때만 문장을 수행하고, 거짓일 때 for문 종료
			// 3. 문장 : 일반적으로 블럭 {}를 사용하고 문장 수행
			// 4. 증감식 : 증감연산자 수행 후 조건식 검사로
		
		// while : 조건에 따른 반복, 조건 먼저 검사
		
		// do-while : 조건 나중에 검사
		
	}
}
